package com.greener;

public class LikedList {
    private String likedImage;
    private String likedName;
    private String likedTel;

    public String getLikedImage() {
        return likedImage;
    }

    public void setLikedImage(String likedImage) {
        this.likedImage = likedImage;
    }

    public String getLikedName() {
        return likedName;
    }

    public void setLikedName(String likedName) {
        this.likedName = likedName;
    }

    public String getLikedTel() {
        return likedTel;
    }

    public void setLikedTel(String likedTel) {
        this.likedTel = likedTel;
    }
}
